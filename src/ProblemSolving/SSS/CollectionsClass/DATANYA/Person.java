package ProblemSolving.SSS.CollectionsClass.DATANYA;

public class Person {
    String nama;
    Integer usia;

    public Person(String nama, Integer usia) {
        this.nama = nama;
        this.usia = usia;
    }

    public String getNama() {
        return nama;
    }

    public Integer getUsia() {
        return usia;
    }
}
